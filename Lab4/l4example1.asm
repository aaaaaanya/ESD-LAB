	AREA RESET,DATA,READONLY
	EXPORT __Vectors
	
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	
	ALIGN 
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0,=N
	LDR R0,[R0]
	
	LSR R1,R0,#4 ;gets D
	AND R2,R0,#0XF ;gets 2
	
	CMP R1,#0XA
	ADDLO R1,#0X30
	ADDHS R1,#0X37
	
	CMP R2,#0XA
	ADDLO R2,#0X30
	ADDHS R2,#0X37
	
	LDR R3,=RES
	STR R1,[R3],#4
	STR R2,[R3]
	
	
STOP B STOP	

N DCD 0X2D 

	AREA mydata,DATA,READWRITE
RES DCD 0,0
	END
	
